    <h1 id="h1_personal_data_what_we_need_from_you_and_why">Personal data: what we need from you and why</h1>
    <p>Personal data is only ever collected by us under these circumstances:</p>
    <h2 id="h2_you_wish_to_be_contacted_by_us_when_you_fill_in_our_website_contact_form">You wish to be contacted by us
        when you fill in our website contact form</h2>
    <p>We will need to have your <strong>name</strong> and your <strong>email address</strong> so that we can complete
        your request to provide information to you.</p>
    <h2 id="h2_you_wish_to_be_considered_for_a_position_in_identity_bank">You wish to be considered for a position in
        Identity Bank</h2>
    <p>You have provided us with your CV and/or additional information in, for instance, a covering letter about
        yourself (hereinafter, called "job application information"), or you have instructed a third party work
        placement business to supply us with your job application information.</p>
    <p>In this case you consent to us having your job application information either by sending it to us directly, or
        indirectly, by giving your permission to send this to us via an agency or similar related work placement
        business.</p>
    <h2 id="h2_when_you_sign_up_to_become_an_identity_bank_business_customer">When you sign up to become an Identity
        Bank business customer</h2>
    <p>To create a business account for you we need to know the <strong>name</strong> of the person who will be the
        primary contact for your business. We will also need to have an <strong>email address</strong> and a <strong>phone
            number</strong> for this person to be contacted.</p>
    <p>We need this information so that we can contact the business about administration issues for the business
        account.</p>
    <h2 id="h2_when_you_are_invited_by_an_identity_bank_business_to_create_an_identity_bank_personal_account">When you
        are invited by an Identity Bank business to create an Identity Bank personal account</h2>
    <p>We will need your <strong>name</strong>, <strong>email</strong> and <strong>mobile</strong>
        <strong>number</strong> to open a personal account for you.</p>
    <h1 id="h1_general_data_protection_regulation_gdpr_lawful_bases_we_rely_on_for_processing_personal_data">General
        Data Protection Regulation (GDPR) lawful bases we rely on for processing personal data</h1>
    <p>Lawful bases we do rely on:</p>
    <ul>
        <li>We have a contractual obligation.</li>
        <li>Your consent.</li>
        <li>We have a legal obligation.</li>
    </ul>
    <p>Lawful bases we might rely on:</p>
    <ul>
        <li>We have a legitimate interest.</li>
    </ul>
    <h1 id="h1_what_we_do_with_the_personal_data_you_provide">What we do with the personal data you provide</h1>
    <p>We use the personal data information that you provide us with as follows.</p>
    <h2 id="h2_when_you_fill_in_the_contact_form_on_our_website">When you fill in the contact form on our website</h2>
    <p>We use your <strong>name</strong> and <strong>email</strong> <strong>address</strong> to contact you by email. We
        will contact you once to respond to your enquiry. Twice by email if you want to be notified of Identity Bank
        service availability - just before product release and again when our product is launched. After that we will
        delete your information.</p>
    <h2 id="h2_when_you_wish_to_be_considered_for_a_position_in_identity_bank">When you wish to be considered for a
        position in Identity Bank</h2>
    <p>If your job application does not result in your placement for the position you have applied for within Identity
        Bank, then your job application information will be deleted and permanently removed from our storage – this
        means paper, electronic media, whatever format(s) the job application information was supplied in, all will be
        permanently deleted from our storage system.<br> You are of course welcome to apply for other positions, but you
        will have to resubmit job application information to do this as we won’t hold anything on file, unless you ask
        us in writing to do this.</p>
    <h2 id="h2_when_you_are_the_primary_contact_for_an_identity_bank_business_account">When you are the primary contact
        for an Identity Bank business account</h2>
    <p>We need to be able to contact the person who is responsible for opening the Identity Bank business account and
        for the administration of the business account.</p>
    <p>We will keep your contact details as long as you have an Identity Bank business account with us and that account
        is open, valid and operating under the terms and conditions of the contract you have agreed to when you signed
        up to open your account.</p>
    <h2 id="h2_when_you_have_an_identity_bank_personal_account">When you have an Identity Bank personal account</h2>
    <p>We need your contact details (<strong>name</strong>, <strong>email</strong> and <strong>phone</strong> <strong>number</strong>)
        to open a personal account for you. We need this information to check you are who you claim to be. After the
        account has been opened we keep your contact details in case you need to recover your account.</p>
    <p>We keep your contact details for as long as your personal account is open. Personal accounts that do not have
        links to businesses in Identity Bank will be automatically closed. See the section: "<a
                href="https://docs.google.com/document/d/1pJG6hoCfdnQ-dLCKSTbjPiHF81EdUCWZyqz7aArzp50/edit#heading=h.2s8eyo1">Your
            right to erasure</a>" for more information.</p>
    <h1 id="h1_what_we_dont_do_with_your_personal_data">What we don’t do with your personal data</h1>
    <p>We <span style="text-decoration: underline;">do not</span> share any information with any organizations or
        individuals.</p>
    <h1 id="h1_how_we_store_your_personal_data">How we store your personal data</h1>
    <p>Your information is securely stored in encrypted form within the European Union.</p>
    <p>We keep your personal data for a time period required by the lawful basis with the exception of details sent to
        us using our contact form, see section ‘<a
                href="https://docs.google.com/document/d/1pJG6hoCfdnQ-dLCKSTbjPiHF81EdUCWZyqz7aArzp50/edit#heading=h.ab4zybp7dvga">When
            you fill in the contact form on our website</a>’. We will then erase your personal data.</p>
    <h1 id="h1_your_data_protection_rights">Your data protection rights</h1>
    <p>Under data protection law, you have rights as explained below along with an explanation of how we fulfil your
        rights in Identity Bank.</p>
    <h2 id="h2_your_right_of_access">Your right of access</h2>
    <p><strong>You have the right to access your personal information.</strong></p>
    <p>Identity Bank has two types of accounts - personal and business. In both cases once the initial account is set
        up: We have no access to your account.</p>
    <p><strong>Personal accounts</strong>: You can add, amend or delete data about yourself and decide which businesses
        can see what data about you. The only personal data we have about your account are your contact details
        (<strong>name</strong>, <strong>email</strong> and <strong>phone</strong> <strong>number</strong>). We need this
        information to administer your account and send notifications to your attention about your account.</p>
    <p>We have no access to your personal account data.</p>
    <p><strong>Business accounts</strong>: You upload your own customer data and/or business files, this means that you
        decide what information you store about your customers and what business files are in your business account. The
        only personal data we have about your business account is the information we need to administer your account,
        that is, the business account holder’s <strong>name</strong>, <strong>email</strong> and <strong>phone</strong>
        <strong>number</strong>.</p>
    <p>We have no access to your business account data. &nbsp;</p>
    <h2 id="h2_your_right_to_rectification">Your right to rectification</h2>
    <p><strong>You have the right to rectify information you think is inaccurate. You also have the right to complete
            information you think is incomplete.</strong></p>
    <p><strong>Personal accounts</strong>: you have full access to your own personal information, It is your account and
        you can add, amend and delete your personal data.</p>
    <p>For example, if you see inaccurate information held about you by a business you can change that information
        yourself. This latest information will then be available to the business(es) that you have allowed to have that
        information about you.</p>
    <p>We cannot amend any personal information about you. You are in control of your personal account and only you have
        access to do this.</p>
    <p><strong>Business accounts</strong>: the primary business account holder can amend or update their personal data
        profile information (<strong>name</strong>, <strong>email</strong> and <strong>phone</strong>
        <strong>number</strong>) within their account. Identity Bank will then be notified of this action.</p>
    <h2 id="h2_your_right_to_erasure">Your right to erasure</h2>
    <p><strong>You have the right to erase your personal information in certain circumstances.</strong></p>
    <p><strong>Personal accounts</strong>: you have full access to your own personal information in your account. You
        can delete personal data that you have entered about yourself into your account, or your personal account
        anytime you like.</p>
    <p>There are some conditions however, under which you cannot remove data about yourself and/or delete your account
        or you might want to rethink these actions, these are explained below.</p>
    <p><strong>Example 1: You have an association with an entity that is legally bound to keep personal data about
            you.</strong></p>
    <p>This entity is likely to be a national body or organization or some form of business that is obliged by national
        law to keep certain information about you. Such entities are subject to specific additional rules on top of the
        GDPR rules.</p>
    <p><strong>Example 2: You have entered into a transaction with a business to supply goods to you and the transaction
            is in progress, but not complete.</strong></p>
    <p>In such a case if you delete your account or deny access to the business, to, for example, your name and address.
        Then it is not in your interest to do this as the business will be unable to deliver to you the goods you have
        ordered without this information.</p>
    <p>We cannot erase any personal information stored in your personal account about you. You are in control of your
        personal account and you alone have the access to do this task yourself.</p>
    <h3 id="h3_identity_bank_account_closures">Identity Bank Account Closures</h3>
    <p>This section explains the circumstances under which either personal or business account can be closed and what we
        do with personal data under these circumstances.</p>
    <p><strong>Personal accounts</strong>: There are two circumstances under which a personal account can be closed.</p>
    <ol>
        <li><strong>If you decide to close your personal account.</strong><br> In this case we will inform your
            associated businesses of your decision and once all these businesses accept your decision, then the
            associations will be terminated.
        </li>
        <li><strong>If we decide to close your personal account.</strong><br> We will close your personal account if you
            have no association with any Identity Bank businesses.
        </li>
    </ol>
    <p>At this point there are no more associated businesses in your account, and your account will be erased from our
        storage systems within 30 days. If you associate a business during these 30 days then the account stays
        operational. After these 30 days you will have to create a new personal account.</p>
    <p><strong>Business accounts</strong>: Business account closures are bound under the terms and conditions entered
        into in the contract which you signed up to when you opened the business account. &nbsp;</p>
    <p>The personal data details (<strong>name</strong>, <strong>email</strong> and <strong>phone</strong> <strong>number</strong>)
        of the business account holder will be permanently deleted from our storage systems within 30 days after the
        account has been closed.</p>
    <h2 id="h2_your_right_to_restriction_of_processing">Your right to restriction of processing</h2>
    <p><strong>You have the right to ask us to restrict the processing of your information in certain
            circumstances.</strong></p>
    <p>We have explained below what processing we do on personal data that you provide to us.</p>
    <p><strong>Personal Accounts</strong>: we keep your <strong>name</strong>, <strong>email</strong> and
        <strong>phone</strong> <strong>number</strong> personal data and we use this information (process this data) as
        follows:</p>
    <ul>
        <li>to open a personal account for you</li>
        <li>to send notifications to your personal account that an issue needs your attention</li>
        <li>to verify your identity in case of account access recovery</li>
    </ul>
    <p><strong>Business accounts</strong>: we keep the business account holder’s <strong>name</strong>,
        <strong>email</strong> and <strong>phone</strong> <strong>number</strong> personal data and we use this
        information (process this data) to administer your business account as follows:</p>
    <ul>
        <li>to open a business account for your business</li>
        <li>to send notifications to your business account that an issue needs your attention</li>
        <li>to restore your account if it has been inadvertently deleted</li>
        <li>to verify your identity in case of account access recovery</li>
    </ul>
    <p>Furthermore, for both personal and business accounts:</p>
    <ul>
        <li>We do not do any further processing on your personal data.</li>
        <li>We do not share your personal data with any third parties.</li>
    </ul>
    <h2 id="h2_your_right_to_data_portability">Your right to data portability</h2>
    <p>You are in charge of your personal account. This means you get to decide what personal data gets transferred to,
        or accessed by, another business, not us. We will facilitate a transfer of your personal data into, out of, or
        within Identity Bank. Full Instructions can be found inside the portal on how to do this.</p>
